// Drift sentinel #1 — src/types/db.generated.ts vs live Supabase schema.
//
// Plan ref: test-reclassification-plan.md § 5.1 P0-A2.
//
// What it does:
//   1. Calls `npx supabase gen types typescript --project-id <ref>` against
//      vera-prod, capturing fresh stdout.
//   2. Diffs the freshly-generated text against the committed file at
//      src/types/db.generated.ts.
//   3. Exits non-zero on drift so CI blocks any PR whose db.generated.ts has
//      not been regenerated after a schema change.
//
// Required env to perform the live check:
//   SUPABASE_ACCESS_TOKEN  — personal access token (sbp_...)
//   SUPABASE_PROJECT_ID    — defaults to "etxgvkvxvbyserhrugjw" (vera-prod)
//
// If SUPABASE_ACCESS_TOKEN is absent (typical for fork PRs without secrets),
// the script falls back to a non-blocking smoke test: it asserts the file
// exists, is non-empty, and contains the expected `export type Database`
// marker. That keeps the sentinel useful in unprivileged CI contexts while
// still failing loudly when a privileged context (push to main) has drift.

import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";

const REPO_ROOT = process.cwd();
const TYPES_PATH = path.join(REPO_ROOT, "src/types/db.generated.ts");
const PROJECT_ID = process.env.SUPABASE_PROJECT_ID || "etxgvkvxvbyserhrugjw";
const ACCESS_TOKEN = process.env.SUPABASE_ACCESS_TOKEN;

function fail(msg) {
  console.error(`✗ check-db-types: ${msg}`);
  process.exit(1);
}

function ok(msg) {
  console.log(`✓ check-db-types: ${msg}`);
}

if (!fs.existsSync(TYPES_PATH)) {
  fail(`missing ${path.relative(REPO_ROOT, TYPES_PATH)} — run \`npx supabase gen types typescript --project-id ${PROJECT_ID}\` and commit the output`);
}

const committed = fs.readFileSync(TYPES_PATH, "utf8");

if (committed.length < 1000) {
  fail(`${path.relative(REPO_ROOT, TYPES_PATH)} looks truncated (${committed.length} bytes)`);
}
if (!committed.includes("export type Database")) {
  fail(`${path.relative(REPO_ROOT, TYPES_PATH)} missing \`export type Database\` marker`);
}

if (!ACCESS_TOKEN) {
  console.warn("⚠ SUPABASE_ACCESS_TOKEN not set — skipping live diff (file-existence check only).");
  console.warn("  Set the secret in GitHub Actions to enable full drift detection.");
  ok(`db.generated.ts present (${committed.length} bytes)`);
  process.exit(0);
}

console.log(`→ Generating fresh types from project ${PROJECT_ID}…`);
const res = spawnSync(
  "npx",
  ["--yes", "supabase@latest", "gen", "types", "typescript", "--project-id", PROJECT_ID, "--schema", "public"],
  { encoding: "utf8", env: { ...process.env, SUPABASE_ACCESS_TOKEN: ACCESS_TOKEN } },
);

if (res.status !== 0) {
  console.error(res.stderr || res.stdout);
  fail("supabase gen types exited non-zero");
}

// Strip the autogenerated header that we prepend at write-time so the diff
// only compares the actual type declarations.
const stripHeader = (s) => s.replace(/^(\/\/[^\n]*\n)+/, "").trimStart();
const fresh = stripHeader(res.stdout);
const stored = stripHeader(committed);

if (fresh !== stored) {
  // Print a small head/tail diff context to make the failure debuggable in CI.
  const freshLines = fresh.split("\n");
  const storedLines = stored.split("\n");
  let firstDrift = -1;
  for (let i = 0; i < Math.max(freshLines.length, storedLines.length); i++) {
    if (freshLines[i] !== storedLines[i]) {
      firstDrift = i;
      break;
    }
  }
  console.error("✗ DRIFT detected between committed db.generated.ts and live schema.");
  console.error(`  First diverging line: ${firstDrift + 1}`);
  if (firstDrift >= 0) {
    console.error("  --- committed ---");
    console.error("  " + (storedLines[firstDrift] ?? "<EOF>"));
    console.error("  +++ fresh +++");
    console.error("  " + (freshLines[firstDrift] ?? "<EOF>"));
  }
  console.error("  Fix: regenerate the file and commit:");
  console.error(`    npx supabase gen types typescript --project-id ${PROJECT_ID} --schema public > src/types/db.generated.ts`);
  process.exit(1);
}

ok(`live schema matches committed file (${committed.length} bytes)`);
